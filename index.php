        <br>Por isso, apresentamos o professor Nando Poeta, cordelista e sociólogo, junto com o estudante Tiago Silva, para a Prefeitura de Natal. Com eles, estão os lutadores do mandato coletivo para a Câmara Municipal, com Alexandre Guedes encabeçando o coletivo, composto também por Érica Guarani e José Jairan, além da professora Luciana Lima. São todos trabalhadores que lutam diariamente contra o descaso e os ataques dos governos, defendendo propostas socialistas para enfrentar os problemas causados pelo capitalismo. Venha com a gente fazer parte desse movimento, pensar, lutar e mudar a nossa cidade. Vamos juntos construir um projeto socialista para Natal!
