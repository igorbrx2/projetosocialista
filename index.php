        <br>Nestas eleições, o PSTU vai apresentar uma alternativa com independência de classe, revolucionária e socialista, para os ativistas e trabalhadores de Natal. Mas nós queremos fazer isso junto com você, que nos conhece das lutas do movimento sindical, das greves, protestos de rua e das manifestações populares. Esse é o nosso lugar e é de onde vamos partir para mudar nossa cidade. Queremos construir uma alternativa que enfrente a oligarquia dos Alves de Carlos Eduardo, a barbárie da extrema direita bolsonarista de Álvaro Dias e Paulinho Freire, mas também uma alternativa de oposição de esquerda ao PT de Natália e Lula, que vivem de alianças com as elites e seus partidos.<br>

        <br>Para mudar Natal de verdade, é preciso enfrentar todos eles e defender um programa socialista que ataque os privilégios dos ricos e dos poderosos, um programa contra o capitalismo, a exploração, as injustiças e as desigualdades. Um verdadeiro projeto socialista para nossa cidade deve:<br>

        <br>1) Colocar o Seturn para fora do sistema de transporte, reduzir o valor da passagem, garantir passe livre para os estudantes e os desempregados, e criar uma empresa pública de mobilidade urbana, culminando em um sistema integrado, com trens, VLTs, ônibus e ciclovias;<br>

        <br>2) Acolher as pessoas em situação de rua, construir abrigos públicos e mapear e desapropriar imóveis sem função social (públicos e privados) para fins de moradia popular;<br>

