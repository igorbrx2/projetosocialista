      <h2 class="montserrat-font">DO CAOS À ESPERANÇA:<br> O caminho para uma Natal Socialista</h2>
      <p>Natal não é a mesma para todos. Nossa cidade não oferece o mínimo de infraestrutura nas periferias, como saneamento básico e espaços de lazer e cultura. O transporte público, que deveria ser um direito essencial, é controlado há décadas por uma máfia de empresários. Nas creches, as vagas são definidas por sorteio e nossos filhos não têm o direito de estudar.<br>

        <br>Na saúde, a população só encontra UPAs lotadas, Unidades Básicas precárias, maternidade e hospital desmantelados. Faltam leitos, exames, equipamentos e muitos profissionais. O colapso só não é total graças ao sacrifício dos servidores. Enquanto isso, recursos do SUS escorrem para o setor privado, através de terceirizações e da privatização do serviço. Nem mesmo a dignidade de ter um lugar para morar é garantida, com um número crescente de pessoas em situação de rua, desamparadas, esquecidas no desemprego e na miséria. Definitivamente, Natal não é a mesma cidade para todos.<br>

        <br><strong>Isso é obra do capitalismo... e de seus representantes</strong><br>

        <br>Esse cenário é apenas um pequeno retrato do caos e do descaso do sistema capitalista. Resultado de anos de políticas dos ricos e poderosos, na Prefeitura e na Câmara de Natal, sejam eles de direita, extrema direita ou daqueles que se dizem de “esquerda”, mas que governam para as elites. Junto com a maioria dos vereadores, o principal responsável hoje é o prefeito Álvaro Dias (Republicanos), que na pandemia juntou-se com Bolsonaro e sua política negacionista, o que custou a vida de centenas de milhares de pessoas. Álvaro Dias já deu calote nos artistas de Natal, não paga os direitos dos servidores públicos e ainda beneficia a máfia do Seturn com isenção de impostos, enquanto o povo fica sem transporte e paga caro pela passagem.<br>

        <br>Agora, o prefeito tenta emplacar o candidato Paulinho Freire (União Brasil), com o apoio de nomes da extrema direita, como Styvenson Valentim (Podemos) e Rogério Marinho (PL), inimigos dos direitos trabalhistas. Paulinho Freire já foi vice da ex-prefeita Micarla de Sousa, que liderou um esquema de corrupção envolvendo a privatização da gestão de serviços de saúde em Natal. Também na direita, o ex-prefeito Carlos Eduardo Alves (PSD), de forma oportunista, tenta aparecer como salvador da pátria, como se não fosse culpado pelos problemas da cidade que ele já governou. Representante de oligarquia, Carlos Eduardo foi responsável por colocar Álvaro Dias na Prefeitura, já esteve com Bolsonaro em 2018, e depois com Lula e Fátima em 2022. Quer enganar a quem?!<br>

        <br>Já Natália Bonavides (PT) defende um projeto de aliança com setores dos ricos e poderosos para enfrentar a extrema direita. Mas até agora essa política só serviu para atacar os trabalhadores e fortalecer as propostas da burguesia, como o arcabouço fiscal de Lula e Alckmin, que reduziu os investimentos sociais. Em várias cidades, o PT apoia a direita, como Republicanos e União Brasil, que possuem ministérios no governo Lula e são, respectivamente, os partidos do prefeito Álvaro Dias e do candidato Paulinho Freire.<br>

        <br>Essa é a mesma política de Fátima (PT) no RN, que trouxe o MDB e as oligarquias de volta ao governo, com Walter Alves. O preço dessas alianças é muito alto, mas depois quem paga o prejuízo é o trabalhador. Não se muda a vida do povo em aliança com aqueles que exploram os trabalhadores. Lamentavelmente, esse também tem sido o caminho do PSOL e da UP, que apoiam o PT e sua política de “servir a dois senhores”. Não concordamos com isso.<br>

        <br><strong>É preciso independência de classe e uma alternativa revolucionária e socialista</strong><br>

        <br>Nestas eleições, o PSTU vai apresentar uma alternativa com independência de classe, revolucionária e socialista, para os ativistas e trabalhadores de Natal. Mas nós queremos fazer isso junto com você, que nos conhece das lutas do movimento sindical, das greves, protestos de rua e das manifestações populares. Esse é o nosso lugar e é de onde vamos partir para mudar nossa cidade. Queremos construir uma alternativa que enfrente a oligarquia dos Alves de Carlos Eduardo, a barbárie da extrema direita bolsonarista de Álvaro Dias e Paulinho Freire, mas também uma alternativa de oposição de esquerda ao PT de Natália e Lula, que vivem de alianças com as elites e seus partidos.<br>

        <br>Para mudar Natal de verdade, é preciso enfrentar todos eles e defender um programa socialista que ataque os privilégios dos ricos e dos poderosos, um programa contra o capitalismo, a exploração, as injustiças e as desigualdades. Um verdadeiro projeto socialista para nossa cidade deve:<br>

        <br>1) Colocar o Seturn para fora do sistema de transporte, reduzir o valor da passagem, garantir passe livre para os estudantes e os desempregados, e criar uma empresa pública de mobilidade urbana, culminando em um sistema integrado, com trens, VLTs, ônibus e ciclovias;<br>

        <br>2) Acolher as pessoas em situação de rua, construir abrigos públicos e mapear e desapropriar imóveis sem função social (públicos e privados) para fins de moradia popular;<br>

        <br>3) Acabar com as isenções fiscais para grandes empresas, cobrar as suas dívidas milionárias, taxar de forma progressiva o IPTU e ampliar a isenção para pequenos comerciantes e a população pobre;<br>

        <br>4) Dobrar os investimentos em saúde e educação públicas, universalizar e garantir vagas para todas as crianças em creches e escolas públicas; reestruturar toda a rede municipal de saúde, assegurando um SUS 100% público e estatal, sem terceirização nem privatização do setor;<br>

        <br>5) Respeitar todos os direitos conquistados pelos servidores, realizar concursos públicos para todas as áreas necessárias e valorizar as categorias que fazem funcionar os serviços essenciais em Natal;<br>

        <br>6) Implementar um plano de obras públicas para reerguer a nossa cidade; construir escolas, unidades de saúde, hospitais, fazer saneamento básico e revitalizar os espaços urbanos de arte, cultura e lazer;<br>

        <br>7) Combater toda a opressão racial, de gênero e a LGBTfobia em Natal, com campanhas educativas em escolas e demais espaços; fortalecer a infraestrutura para denunciar os crimes, locais adequados para acolher as vítimas e punição aos agressores;<br>

        <br>8) Reduzir os salários do prefeito, vice, secretários e vereadores de Natal, igualando a remuneração dos políticos ao salário de uma professora do município.<br>

        <br>Queremos discutir essas e outras propostas com o conjunto dos ativistas e trabalhadores de nossa cidade, de modo a ampliar e a fortalecer um programa socialista que ataque de frente os principais problemas de Natal. Mas isso não se faz mantendo o orçamento público, o dinheiro do povo, a serviço dos ricos e poderosos da cidade, tampouco sem enfrentar o capitalismo. A Natal que precisamos tem que ser arrancada, construída na luta, com a nossa força, mobilização e auto-organização. É preciso avançar na construção de um projeto socialista.<br>

        <br>Por isso, apresentamos o professor Nando Poeta, cordelista e sociólogo, junto com o estudante Tiago Silva, para a Prefeitura de Natal. Com eles, estão os lutadores do mandato coletivo para a Câmara Municipal, com Alexandre Guedes encabeçando o coletivo, composto também por Érica Guarani e José Jairan, além da professora Luciana Lima. São todos trabalhadores que lutam diariamente contra o descaso e os ataques dos governos, defendendo propostas socialistas para enfrentar os problemas causados pelo capitalismo. Venha com a gente fazer parte desse movimento, pensar, lutar e mudar a nossa cidade. Vamos juntos construir um projeto socialista para Natal!
      </p>
